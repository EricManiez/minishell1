#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		i;
	char	*ptr;

	i = -1;
	while (s[++i])
	{
		ptr = &s[i];
		f(i, ptr);
	}
}
