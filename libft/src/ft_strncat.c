#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int					i;
	unsigned int		j;

	i = 0;
	j = -1;
	while (s1[i])
		i++;
	while (++j < n)
	{
		s1[i] = s2[j];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
